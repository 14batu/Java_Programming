package m14_condition_statements_part1;

public class Number {
    public static void main(String[] args) {
         int number = 0;

         if (number > 0){
             System.out.println("Positive");
         } else if (number < 0) {

             System.out.println("Negative");
         } else {
             System.out.println("Zero");
         }
    }
}
