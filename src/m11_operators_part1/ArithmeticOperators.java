package m11_operators_part1;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println(10 + 5);
        System.out.println(10 - 5);
        System.out.println(10 / 5);
        System.out.println(10 / 4);
        System.out.println(10.0 / 4);
        System.out.println(10D / 4);
        System.out.println(10 * 5);
        System.out.println(10 % 5);
    }
}
